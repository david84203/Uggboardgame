// 讀取已存在的 bggId
const existingIds = new Set();
const idPattern = /"(\d+)":/g;
let m;
while ((m = idPattern.exec(content)) !== null) {
  existingIds.add(m[1]);
}

const newEntries = batch.filter(([id]) => !existingIds.has(id));
const skipped = batch.filter(([id]) => existingIds.has(id)).map(([id]) => id);

if (skipped.length) console.log('跳過已存在：', skipped.join(', '));
if (!newEntries.length) { console.log('無新增項目'); process.exit(0); }

const match = content.match(/\n(\t*)\};/);
const idx = match.index;
const tab = match[1];

let insertText = '\n\t  // === Batch 7: entries 300-314 ===';
for (const [id, desc] of newEntries) {
  insertText += '\n\t  "' + id + '": "' + desc + '",';
}

content = content.slice(0, idx) + insertText + '\n' + tab + '};';
fs.writeFileSync(target, content, 'utf8');
console.log('完成，寫入 ' + newEntries.length + ' 筆');
